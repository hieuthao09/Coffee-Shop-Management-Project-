    private void btn_themMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_themMouseClicked
        // TODO add your handling code here:
        String tenKM = txt_tenkm.getText();
        String tile = txt_tile.getText();
        Float kq = Float.parseFloat(tile)/100;
        km.setTenkm(tenKM);
        km.setTile(kq);
        exdt.insertKM(km);
            JOptionPane.showMessageDialog(this, "Thêm Thành Công");
        resetText();
        showDataOnTable();
    }//GEN-LAST:event_btn_themMouseClicked

    private void tbl_khuyenmaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_khuyenmaiMouseClicked
        // TODO add your handling code here:
        int row = tbl_khuyenmai.getSelectedRow();
        if (row != -1) {
            txt_tenkm.setText(tbl_khuyenmai.getValueAt(row, 1).toString());
            txt_tile.setText(tbl_khuyenmai.getValueAt(row, 2).toString()); 
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn dòng nào!!!");
        }
    }//GEN-LAST:event_tbl_khuyenmaiMouseClicked

    private void btn_xoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_xoaMouseClicked
        // TODO add your handling code here:
        int row = tbl_khuyenmai.getSelectedRow();
        km.setMakm(tbl_khuyenmai.getValueAt(row, 0).toString());
        exdt.deleteKM(km); 
            JOptionPane.showMessageDialog(this, "Xóa Thành Công");
        resetText();
        showDataOnTable();
    }//GEN-LAST:event_btn_xoaMouseClicked

    private void btn_suaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_suaMouseClicked
        // TODO add your handling code here:
        String tenKM = txt_tenkm.getText();
        String tiLe = txt_tile.getText();
        int row = tbl_khuyenmai.getSelectedRow();
        km.setMakm(tbl_khuyenmai.getValueAt(row, 0).toString());
        km.setTenkm(tenKM);
        km.setTile(Float.parseFloat(tiLe));
        exdt.updateKM(km);
            JOptionPane.showMessageDialog(this, "Sửa Thành Công");
        resetText();
        showDataOnTable(); 
    }//GEN-LAST:event_btn_suaMouseClicked

