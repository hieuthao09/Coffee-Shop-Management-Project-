    public void showDataOnTable(){
        DefaultTableModel model = (DefaultTableModel)tbl_khuyenmai.getModel();
        x = new GetData();
        ArrayList arr =  x.getDataKM();
        String[] tenCot = {"Mã Khuyễn mãi","Tên khuyến mãi","Tỉ lệ"};
        model.setDataVector((Object[][])arr.get(1), tenCot);
        tbl_khuyenmai.setRowHeight(50);
    }
