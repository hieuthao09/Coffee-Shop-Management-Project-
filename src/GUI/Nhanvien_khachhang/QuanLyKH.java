    private void btn_themMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_themMouseClicked
        // TODO add your handling code here:
        try {
            String tenKH = txt_tenKH.getText();
            String gioitinh = txt_gioiTinh.getText();
            String sdt = txt_sdt.getText();
            String tl = txt_diemTL.getText();
            int dtl = Integer.parseInt(tl);
            Calendar calendar = Calendar.getInstance();
            calendar.setTime(dcs_hsd.getDate());
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String t = dateFormat.format(calendar.getTime());
            kh.setTenkh(tenKH);
            kh.setGioitinh(gioitinh);
            kh.setSdt(sdt);
            kh.setDiemtichluy(String.format("%s", dtl));
            kh.setHsd(t);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn thông tin đầy đủ hoặc " + ex.getMessage());
            return;
        }
        if (ExecuteData.insertKH(kh)) {
            JOptionPane.showMessageDialog(this, "Thêm Thành Công");
            resetText();
            showDataOnTable();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }

    }//GEN-LAST:event_btn_themMouseClicked

    private void btn_xoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_xoaMouseClicked
        // TODO add your handling code here:
        int row = tbl_khachhang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn đối tượng muốn xóa");
            return;
        }
        kh.setMakh(tbl_khachhang.getValueAt(row, 0).toString());
        if (ExecuteData.deleteKH(kh)) {
            JOptionPane.showMessageDialog(this, "Xóa Thành Công");
            resetText();
            showDataOnTable();
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }

    }//GEN-LAST:event_btn_xoaMouseClicked

