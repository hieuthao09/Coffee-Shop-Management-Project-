    private void tbl_khachhangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_khachhangMouseClicked
        // TODO add your handling code here:
        int row = tbl_khachhang.getSelectedRow();
        if (row != -1) {
            txt_tenKH.setText(tbl_khachhang.getValueAt(row, 1).toString());
            txt_gioiTinh.setText(tbl_khachhang.getValueAt(row, 2).toString());
            txt_sdt.setText(tbl_khachhang.getValueAt(row, 3).toString());
            txt_diemTL.setText(tbl_khachhang.getValueAt(row, 4).toString());
            Date date = new Date();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            String dateStr = tbl_khachhang.getValueAt(row, 5).toString();
            try {
                dcs_hsd.setDate(dateFormat.parse(dateStr));
            } catch (ParseException ex) {
                Logger.getLogger(Fr_phieuNhap1.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn dòng nào!!!");
        }
    }//GEN-LAST:event_tbl_khachhangMouseClicked


