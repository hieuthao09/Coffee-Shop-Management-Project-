    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        // TODO add your handling code here:
        int row = table1.getSelectedRow();
        txt_ncc.setText(table1.getValueAt(row, 2).toString());
        txt_dongia.setText(table1.getValueAt(row, 1).toString());
        txt_soluong.setText(table1.getValueAt(row, 0).toString());
        ThucDon t = new ThucDon();
        for(int i = 0 ; i < cbo_sp.getItemCount();i++)
        {
            t = (ThucDon) cbo_sp.getItemAt(i);
            if(t.getId().equals(table1.getValueAt(row, 4).toString()))
            {
                cbo_sp.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_table1MouseClicked

    void loadCTPhieuNhap(String ma) {
        DefaultTableModel model = (DefaultTableModel) table1.getModel();
        String[] tenCot = {"Mã Phiếu nhập", "Ngày nhập", "Nhân viên"};
        model.setDataVector(getdata.getDataCTPhieuNhap(ma), tenCot);
    }

    DefaultComboBoxModel<ThucDon> model_cbo_td ;
    void loadcomboNV()
    {
        // Khởi tạo một danh sách các chuỗi
        Object[][] info1 = (Object[][])getdata.getDataDSSP().get(1);
        model_cbo_td = new DefaultComboBoxModel<>();
        for (Object[] innerList : info1) {
            ThucDon td = new ThucDon();
            if (innerList[0] instanceof String) {
                td.setId((String) innerList[0]);

            }
            if (innerList[1] instanceof String) {
                td.setName((String) innerList[1]);
            }
            model_cbo_td.addElement(td);
        }
        cbo_sp.setModel(model_cbo_td);
    }
