    private void btn_ThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ThemMouseClicked
        // TODO add your handling code here:
        ChiTietPhieuNhap pn = new ChiTietPhieuNhap();
        if(txt_soluong.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập số lượng");
            return;
        }
        else
        {
            if(txt_dongia.getText().isEmpty() || txt_ncc.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Bạn chưa điền đầy đủ thông tin");
                return;
            }
            int sl = Integer.parseInt(txt_soluong.getText());
            if(sl>=0)
            {
                pn.setSoluong(sl);
                float gia = Float.parseFloat(txt_dongia.getText());
                if(gia<0)
                {
                    JOptionPane.showMessageDialog(this, "Giá không được bé hơn 0");
                    return; 
                }
                pn.setGianhap(gia);
                pn.setTenncc(txt_ncc.getText());
                ThucDon td = (ThucDon) cbo_sp.getSelectedItem();
                
                pn.setNguyenlieumanl(td.getId());
                pn.setPhieunhapmapn(mapn);
                if(ExecuteData.insertCTPN(pn))
                {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    return; 
                }else
                {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                    return; 
                }
            }
            else
            {
                
                    JOptionPane.showMessageDialog(this, "Số lượng không được bé hơn 0");
                    return; 
            }
        }
        
        
        
        
    }//GEN-LAST:event_btn_ThemMouseClicked

