    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        // TODO add your handling code here:
        int row = table1.getSelectedRow();
        txt_ncc.setText(table1.getValueAt(row, 2).toString());
        txt_dongia.setText(table1.getValueAt(row, 1).toString());
        txt_soluong.setText(table1.getValueAt(row, 0).toString());
        NguyenLieu t = new NguyenLieu();
        String chuoi =null;
        for(int i = 0 ; i < cbo_sp.getItemCount();i++)
        {
            chuoi = table1.getValueAt(row, 3).toString();
            t = (NguyenLieu) cbo_sp.getItemAt(i);
            if(t.getManl().equals(chuoi))
            {
                cbo_sp.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_table1MouseClicked

    private void btn_ThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_ThemMouseClicked
        // TODO add your handling code here:
        ChiTietPhieuNhap pn = new ChiTietPhieuNhap();
        if(txt_soluong.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập số lượng");
            return;
        }
        else
        {
            if(txt_dongia.getText().isEmpty() || txt_ncc.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Bạn chưa điền đầy đủ thông tin");
                return;
            }
            int sl = Integer.parseInt(txt_soluong.getText());
            if(sl>=0)
            {
                pn.setSoluong(sl);
                float gia = Float.parseFloat(txt_dongia.getText());
                if(gia<0)
                {
                    JOptionPane.showMessageDialog(this, "Giá không được bé hơn 0");
                    return;
                }
                pn.setGianhap(gia);
                pn.setTenncc(txt_ncc.getText());
                NguyenLieu td = (NguyenLieu) cbo_sp.getSelectedItem();

                pn.setNguyenlieumanl(td.getManl());
                pn.setPhieunhapmapn(mapn);
                if(ExecuteData.insertCTPN(pn))
                {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    loadCTPhieuNhap(mapn);
                    return;
                }else
                {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                    return;
                }
            }
            else
            {

                JOptionPane.showMessageDialog(this, "Số lượng không được bé hơn 0");
                return;
            }
        }

    }//GEN-LAST:event_btn_ThemMouseClicked

