    void loadCTPhieuNhap(String ma) {
        DefaultTableModel model = (DefaultTableModel) table1.getModel();
        String[] tenCot = {"Số lượng", "Giá bán", "Tên nhà cung cấp","Mã nguyên liệu"};
        model.setDataVector(getdata.getDataCTPhieuNhap(ma), tenCot);
        table1.setRowHeight(50);
    }

    DefaultComboBoxModel<NguyenLieu> model_cbo_nl ;
    void loadcomboNL()
    {
        // Khởi tạo một danh sách các chuỗi
        Object[][] info1 = (Object[][])getdata.getDataKhoNguyenLieu();
        model_cbo_nl = new DefaultComboBoxModel<>();
        for (Object[] innerList : info1) {
            NguyenLieu nl = new NguyenLieu();
            if (innerList[0] instanceof String) {
                nl.setManl((String) innerList[0]);

            }
            if (innerList[1] instanceof String) {
                nl.setTennl((String) innerList[1]);
            }
            model_cbo_nl.addElement(nl);
        }
        cbo_sp.setModel(model_cbo_nl);
    }
    
